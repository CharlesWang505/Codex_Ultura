function isTauriRuntime() {
  return typeof window !== 'undefined' && '__TAURI_INTERNALS__' in window
}

function loadLanguage(): AppLanguage {
  try {
    const stored = localStorage.getItem(LANGUAGE_KEY)
    if (stored === 'zh-CN' || stored === 'en-US') return stored
  } catch {
    // ignore
  }
  return 'zh-CN'
}

export function translateToEnglish(source: string) {
  if (!/[\u3400-\u9fff]/u.test(source)) return source
  const leading = source.match(/^\s*/u)?.[0] ?? ''
  const trailing = source.match(/\s*$/u)?.[0] ?? ''
  const text = source.trim()
  if (!text) return source
  const exact = exactEnglish[text]
  if (exact) return `${leading}${exact}${trailing}`
  for (const [pattern, replacement] of dynamicEnglish) {
    if (pattern.test(text)) return `${leading}${text.replace(pattern, replacement)}${trailing}`
  }
  let translated = text
  for (const [chinese, english] of phraseEnglish) {
    translated = translated.replaceAll(chinese, english)
  }
  translated = translated
    .replaceAll('，', ', ')
    .replaceAll('、', ', ')
    .replaceAll('。', '.')
    .replaceAll('：', ': ')
    .replaceAll('；', '; ')
    .replaceAll('（', ' (')
    .replaceAll('）', ') ')
    .replace(/\s{2,}/g, ' ')
    .trim()
  return `${leading}${translated}${trailing}`
}

const textRecords = new WeakMap<Text, { source: string; translated: string }>()
const attributeRecords = new WeakMap<Element, Map<string, { source: string; translated: string }>>()
const translatedAttributes = ['title', 'aria-label', 'placeholder', 'alt']

function shouldSkip(node: Node) {
  const parent = node instanceof Element ? node : node.parentElement
  return Boolean(parent?.closest(
    'code, pre, textarea, [contenteditable="true"], [data-i18n-skip], .message-list, .remote-monitor-transcript',
  ))
}

function applyTextNode(node: Text, language: AppLanguage) {
  if (shouldSkip(node)) return
  const current = node.nodeValue ?? ''
  const previous = textRecords.get(node)
  if (language === 'zh-CN') {
    if (previous && current === previous.translated) node.nodeValue = previous.source
    return
  }
  if (previous && current === previous.translated) return
  const translated = translateToEnglish(current)
  textRecords.set(node, { source: current, translated })
  if (translated !== current) node.nodeValue = translated
}

function applyAttributes(element: Element, language: AppLanguage) {
  if (shouldSkip(element)) return
  let records = attributeRecords.get(element)
  if (!records) {
    records = new Map()
    attributeRecords.set(element, records)
  }
  for (const attribute of translatedAttributes) {
    const current = element.getAttribute(attribute)
    if (!current) continue
    const previous = records.get(attribute)
    if (language === 'zh-CN') {
      if (previous && current === previous.translated) element.setAttribute(attribute, previous.source)
      continue
    }
    if (previous && current === previous.translated) continue
    const translated = translateToEnglish(current)
    records.set(attribute, { source: current, translated })
    if (translated !== current) element.setAttribute(attribute, translated)
  }
}

function applyLanguageToTree(root: Node, language: AppLanguage) {
  if (root instanceof Text) {
    applyTextNode(root, language)
    return
  }
  if (root instanceof Element) applyAttributes(root, language)
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_ELEMENT | NodeFilter.SHOW_TEXT)
  let node = walker.nextNode()
  while (node) {
    if (node instanceof Text) applyTextNode(node, language)
    else if (node instanceof Element) applyAttributes(node, language)
    node = walker.nextNode()
  }
}

